#!/bin/bash

# =============================================================================
# Telegram Lite - Автоматический Деплой Скрипт
# =============================================================================
# Использование: ./deploy.sh
# =============================================================================

set -e  # Остановить скрипт при ошибке

# Цвета для вывода
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # Без цвета

# Логотип
echo ""
echo -e "${BLUE}╔════════════════════════════════════════════════════════╗${NC}"
echo -e "${BLUE}║${NC}       🚀 Telegram Lite - Deploy Script          ${BLUE}║${NC}"
echo -e "${BLUE}╚════════════════════════════════════════════════════════╝${NC}"
echo ""

# Переход в директорию проекта
PROJECT_DIR="${HOME}/test"
cd "$PROJECT_DIR" || { echo -e "${RED}❌ Ошибка: Директория $PROJECT_DIR не найдена${NC}"; exit 1; }

echo -e "${GREEN}✓${NC} Директория: ${YELLOW}$PROJECT_DIR${NC}"
echo ""

# =============================================================================
# Шаг 1: Сохранение локальных изменений
# =============================================================================
echo -e "${BLUE}📦 Шаг 1/6: Сохранение локальных изменений...${NC}"

# Проверка есть ли изменения
if git diff --quiet && git diff --cached --quiet; then
    echo -e "${GREEN}✓${NC} Нет локальных изменений"
else
    echo -e "${YELLOW}⚠${NC} Обнаружены локальные изменения..."
    git stash push -m "auto-stash before deploy $(date +%Y-%m-%d_%H-%M-%S)"
    echo -e "${GREEN}✓${NC} Изменения сохранены в stash"
fi
echo ""

# =============================================================================
# Шаг 2: Обновление из репозитория
# =============================================================================
echo -e "${BLUE}📥 Шаг 2/6: Обновление из репозитория...${NC}"

git pull origin main
if [ $? -eq 0 ]; then
    echo -e "${GREEN}✓${NC} Репозиторий обновлён"
else
    echo -e "${RED}❌ Ошибка при обновлении репозитория${NC}"
    exit 1
fi
echo ""

# =============================================================================
# Шаг 3: Восстановление локальных изменений
# =============================================================================
echo -e "${BLUE}📤 Шаг 3/6: Восстановление локальных изменений...${NC}"

STASH_COUNT=$(git stash list | wc -l)
if [ "$STASH_COUNT" -gt 0 ]; then
    git stash pop
    echo -e "${GREEN}✓${NC} Локальные изменения восстановлены"
else
    echo -e "${YELLOW}⚠${NC} Нет сохранённых изменений в stash"
fi
echo ""

# =============================================================================
# Шаг 4: Установка зависимостей
# =============================================================================
echo -e "${BLUE}📦 Шаг 4/6: Установка зависимостей...${NC}"

# Проверка изменился ли package.json
if git diff HEAD~1 --name-only | grep -q "package.json"; then
    echo -e "${YELLOW}⚠${NC} package.json изменён, устанавливаем зависимости..."
    npm install
    echo -e "${GREEN}✓${NC} Зависимости установлены"
else
    echo -e "${GREEN}✓${NC} package.json не изменён, пропускаем установку"
fi
echo ""

# =============================================================================
# Шаг 5: Сборка контейнеров
# =============================================================================
echo -e "${BLUE}🔨 Шаг 5/6: Сборка контейнеров...${NC}"

# Спросить какие контейнеры пересобрать
echo "Какие контейнеры пересобрать?"
echo "1) Все (web + server)"
echo "2) Только web"
echo "3) Только server"
echo "4) Пропустить сборку"
read -p "Выбор [1-4]: " BUILD_CHOICE

case $BUILD_CHOICE in
    1)
        echo -e "${YELLOW}⚠${NC} Пересборка всех контейнеров..."
        docker compose build --no-cache web server
        ;;
    2)
        echo -e "${YELLOW}⚠${NC} Пересборка web..."
        docker compose build --no-cache web
        ;;
    3)
        echo -e "${YELLOW}⚠${NC} Пересборка server..."
        docker compose build --no-cache server
        ;;
    4)
        echo -e "${YELLOW}⚠${NC} Пропускаем сборку"
        ;;
    *)
        echo -e "${RED}❌ Неверный выбор, пересобираем всё${NC}"
        docker compose build --no-cache web server
        ;;
esac

echo -e "${GREEN}✓${NC} Сборка завершена"
echo ""

# =============================================================================
# Шаг 6: Перезапуск сервисов
# =============================================================================
echo -e "${BLUE}🔄 Шаг 6/6: Перезапуск сервисов...${NC}"

docker compose up -d
echo -e "${GREEN}✓${NC} Сервисы запущены"
echo ""

# =============================================================================
# Проверка статуса
# =============================================================================
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo -e "${BLUE}📊 Статус сервисов:${NC}"
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
docker compose ps

echo ""
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo -e "${GREEN}✅ Деплой завершён успешно!${NC}"
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo ""
echo -e "🌐 Web:     ${YELLOW}http://212.119.42.176:3000${NC}"
echo -e "🔌 API:     ${YELLOW}http://212.119.42.176:3001/api/v1${NC}"
echo -e "💚 Health:  ${YELLOW}http://212.119.42.176:3001/api/v1/health${NC}"
echo ""

# Предложение посмотреть логи
read -p "Показать логи web? (y/n): " SHOW_LOGS
if [ "$SHOW_LOGS" = "y" ] || [ "$SHOW_LOGS" = "Y" ]; then
    echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo -e "${BLUE}📋 Логи web (последние 20 строк):${NC}"
    echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    docker compose logs --tail=20 web
fi

echo ""
echo -e "${GREEN}🎉 Готово!${NC}"
echo ""
